def _title(rec):
    """Заглавие записи (200^a) для логов и дедупликации."""
    f200 = rec.get("200") or [{}]
    return (f200[0].get("a") or "").strip()


def _already_present(store, db, title):
    """True, если запись с таким заглавием уже есть (поиск по словарю T=)."""
    if not title:
        return False
    try:
        res = store.search(db, 'T=%s' % title, limit=1)
    except Exception:  # noqa: BLE001 — на пустой/новой БД поиск может бросить
        return False
    # CatalogStore.search() возвращает {total, items:[...]}; поддержим и
    # альтернативные формы (список mfn / dict с found/mfns) на случай иных версий.
    if isinstance(res, dict):
        if res.get("total"):
            return True
        return bool(res.get("items") or res.get("found") or res.get("mfns"))
    return bool(res)


def main():
    print("Демо-каталог: own-store CATALOG_DB=%s, БД=%s" % (CATALOG_DB, DB))
    store = CatalogStore(CATALOG_DB)
    # На свежем own-store создаём схему (идемпотентно).
    try:
        store.ensure_schema()
    except Exception:  # noqa: BLE001 — некоторые версии создают схему в __init__
        pass

    saved, skipped, failed = 0, 0, 0
    for rec in DEMO_RECORDS:
        title = _title(rec)
        if _already_present(store, DB, title):
            print("  ~ пропуск (уже есть): %s" % title)
            skipped += 1
            continue
        try:
            res = store.save(DB, rec)
        except Exception as exc:  # noqa: BLE001
            print("  ! ошибка сохранения %r: %s" % (title, exc))
            failed += 1
            continue
        ok = bool(res.get("saved")) if isinstance(res, dict) else bool(res)
        if ok:
            mfn = res.get("mfn") if isinstance(res, dict) else "?"
            print("  + сохранено mfn=%s: %s" % (mfn, title))
            saved += 1
        else:
            print("  ! не сохранено (ФЛК?): %s → %r" % (title, res))
            failed += 1

    total = store.count(DB) if hasattr(store, "count") else "?"
    print("Готово. Сохранено: %d, пропущено: %d, ошибок: %d. Всего в '%s': %s"
          % (saved, skipped, failed, DB, total))
    # Ненулевой код только если НИЧЕГО не удалось и при этом были новые записи.
    return 1 if (failed and saved == 0 and skipped == 0) else 0


if __name__ == "__main__":
    sys.exit(main())
