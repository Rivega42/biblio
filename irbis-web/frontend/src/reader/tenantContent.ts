const RU_MONTH_GEN = ["января", "февраля", "марта", "апреля", "мая", "июня", "июля", "августа", "сентября", "октября", "ноября", "декабря"];

// ISO YYYY-MM-DD → «18 июня 2026». Пустую/некорректную дату возвращает как есть.
export function fmtNewsDate(iso: string): string {
  const m = /^(\d{4})-(\d{2})-(\d{2})$/.exec(iso || "");
  if (!m) return iso || "";
  const y = +m[1], mo = +m[2], d = +m[3];
  if (mo < 1 || mo > 12) return iso;
  return d + " " + RU_MONTH_GEN[mo - 1] + " " + y;
}
