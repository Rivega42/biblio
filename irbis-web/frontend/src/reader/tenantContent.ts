// Редактируемый per-tenant контент главной читательского портала (G1+).
//
// Это «сид»-контент для пилота: блоки «Новости библиотеки», «События и выставки»,
// «О библиотеке» и определения тематических подборок. В отличие от каталожных
// данных (новинки/рубрики приходят из ИРБИС через /api), этот контент
// конфигурируется библиотекой и в пилоте задаётся статически, помечен как
// «редактируемый» (флаг editable у блоков-обёрток). В дальнейшем заменяется на
// GET /api/tenant/content без изменения компонентов.
//
// Тема пилота — Санкт-Петербургская государственная театральная библиотека
// (СПб ГТБ): фактические реквизиты (адрес/часы/контакты) и театральная специфика
// фонда. Содержимое — реалистичные плейсхолдеры в духе учреждения, НЕ копия
// чужого сайта.
import type { IconName } from "../../components/icon/Icon";

export interface NewsItem {
  id: string;
  date: string;        // ISO YYYY-MM-DD — для сортировки и форматирования
  title: string;
  excerpt: string;
  tag?: string;        // короткая метка-рубрика анонса
}

export interface EventItem {
  id: string;
  date: string;        // ISO YYYY-MM-DD — дата начала
  dateLabel?: string;  // человекочитаемый диапазон, если событие длится (выставка)
  title: string;
  place: string;
  kind: "exhibition" | "lecture" | "tour" | "concert" | "meeting";
  excerpt?: string;
}

// Определение тематической подборки: ведёт в поиск по каталогу. Запрос задаётся
// префиксом ИРБИС (K=ключевые слова) и термином; иконка/тон — для оформления полки.
export interface CollectionDef {
  id: string;
  title: string;
  subtitle: string;
  prefix: string;      // область поиска ИРБИС (K/A/T/...)
  query: string;       // поисковый термин
  icon: IconName;      // имя иконки из набора Icon
  tint: number;        // индекс палитры подложек (0..5)
}

export interface AboutInfo {
  name: string;
  blurb: string;       // краткое «о фонде»
  hours: { label: string; value: string }[];
  address: string;
  phone: string;
  email: string;
  site?: string;
}

export interface TenantContent {
  // editable=true → блоки показывают пометку «редактируется библиотекой».
  editable: boolean;
  collections: CollectionDef[];
  news: NewsItem[];
  events: EventItem[];
  about: AboutInfo;
}

